                <p align="justify"><strong>14 anos:</strong> Conteúdos  mais violentos e/ou de linguagem sexual mais acentuada. Exibição a partir das  21h.</p>

                <p align="justify"><strong>16 anos:</strong> Conteúdos  mais violentos ou com conteúdo sexual mais intenso, com cenas de tortura,  suicídio, estupro ou nudez total. Exibição a partir das 22h.</p>

                <p align="justify"><strong>18 anos:</strong> Conteúdos  violentos e sexuais extremos. Cenas de sexo, incesto ou atos repetidos de  tortura, mutilação ou abuso sexual. Exibição a partir das 23h.</p>
